<h2>Política de Privacidad</h2>

<p>Fecha de vigencia: 16 de Octubre de 2023</p>

<p>MadNox (en adelante, "nosotros", "nuestro" o "la empresa") se compromete a proteger y respetar su privacidad. Esta Política de Privacidad describe cómo recopilamos, utilizamos, protegemos y compartimos la información personal proporcionada por los usuarios (en adelante, "usted" o "el usuario") a través de nuestro sitio web.</p>

<h3 class="subtituloTerminos">Información que Recopilamos</h3>
<p>Para poder contactarnos con usted y brindarle información sobre nuestros servicios, recopilamos los siguientes datos personales:</p>

<ul>
    <li>Nombre y apellidos.</li>
    <li>Dirección de correo electrónico.</li>
    <li>Nombre del negocio (opcional).</li>
    <li>Plan de servicio de interés.</li>
    <li>Número de teléfono de contacto.</li>
    <li>Mensaje breve proporcionado por usted.</li>
</ul>

<h3 class="subtituloTerminos">Uso de la Información</h3>
<p>La información personal proporcionada será utilizada únicamente para los siguientes propósitos:</p>

<ul>
    <li>Contactar con usted para responder a sus consultas y proporcionar información sobre nuestros servicios.</li>
    <li>Mantener registros internos relacionados con las interacciones con los usuarios.</li>
    <li>Mejorar nuestros servicios y personalizar la experiencia del usuario en nuestro sitio web.</li>
</ul>

<h3 class="subtituloTerminos">Protección de Datos</h3>
<p>Su información personal se almacenará y procesará de manera segura. Implementamos medidas técnicas y organizativas adecuadas para proteger sus datos contra pérdida, acceso no autorizado o divulgación, y para garantizar que sean tratados de acuerdo con las leyes y regulaciones aplicables.</p>

<h3 class="subtituloTerminos">Compartir Información</h3>
<p>No venderemos, alquilaremos ni cederemos su información personal a terceros con fines comerciales. Sin embargo, podemos compartir su información con proveedores de servicios externos que nos ayuden a brindar nuestros servicios y mejorar su experiencia en nuestro sitio web. Estos proveedores solo accederán a su información en la medida necesaria para cumplir con sus funciones y están sujetos a obligaciones de confidencialidad.</p>

<h3 class="subtituloTerminos">Consentimiento y Retención de Datos</h3>
<p>Al proporcionarnos sus datos personales, usted otorga su consentimiento para el uso de dicha información de acuerdo con los términos de esta Política de Privacidad. Mantendremos sus datos personales durante el tiempo que sea necesario para cumplir con los fines para los que fueron recopilados, a menos que se requiera o permita un período de retención más largo según las leyes aplicables.</p>

<h3 class="subtituloTerminos">Derechos del Usuario</h3>
<p>Usted tiene ciertos derechos con respecto a su información personal, que incluyen el derecho a acceder, corregir, rectificar o eliminar datos inexactos. Para ejercer estos derechos o si tiene alguna pregunta o inquietud sobre la privacidad de sus datos, puede ponerse en contacto con nosotros a través de [información de contacto].</p>

<h3 class="subtituloTerminos">Aceptación de las Políticas</h3>
<p>Al proporcionar sus datos personales y utilizar nuestro sitio web, usted acepta los términos y condiciones de esta Política de Privacidad y los Términos y Condiciones asociados.</p>

<p>Nos reservamos el derecho de modificar esta Política de Privacidad en cualquier momento. Cualquier modificación entrará en vigencia a partir de su publicación en nuestro sitio web.</p>